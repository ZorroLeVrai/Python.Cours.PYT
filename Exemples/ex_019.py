mon_dico = {1:'Un', 2:'Deux', 3:'Trois', 4:'Quatre'}
print(mon_dico)     #{1:'Un', 2:'Deux', 3: 'Trois', 4: 'Quatre'}
print(mon_dico[2])  #Deux
# Ajout d'une valeur dans le dictionnaire
mon_dico[5] = 'Cinq'
print(mon_dico)     #{1:'Un', 2:'Deux', 3: 'Trois', 4: 'Quatre', 5: 'Cinq'}
# Modification d'une valeur du dictionnaire
mon_dico[3] = 'Three'
print(mon_dico[3])  #Three